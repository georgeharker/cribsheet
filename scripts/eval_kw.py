#!/usr/bin/env python3
"""Evaluate a SEMANTIC keyword facet for code symbols on the gold bed.

Premise (from the design session): a lexical BM25 keyword facet is too noisy for code
(generic tech vocab collides, test keyword-bags overlap impl bags). The value of LLM
keywords is as EMBEDDED phrase vectors matched by cosine — a multi-vector semantic
representation of the symbol, matched via MaxSim. This re-evaluates the dense / name-BM25 /
keyword blend once real keywords exist (generated by gen_kws.py → kws.json).

    python scripts/eval_kw.py
"""
from __future__ import annotations
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "vendor" / "llmkit" / "src"))
sys.path.insert(0, str(ROOT / "scripts"))

KWS = ROOT / "scripts" / "eval_data" / "kws.json"


def _cos(a, b):
    return sum(x * y for x, y in zip(a, b))


def mm(xs):
    lo, hi = min(xs), max(xs)
    r = (hi - lo) or 1.0
    return [(x - lo) / r for x in xs]


def main() -> int:
    from crib.app import Crib
    from eval_fusion import GOLD, Feat, SEGMENTS, _cands, order_from, target_rank, c_covpc

    from crib.retrieve import _subtokens

    kws = json.loads(KWS.read_text())
    print(f"loaded keywords for {len(kws)} symbols")
    crib = Crib.open()

    # build feats up front so we can pull each symbol's NAME into its keyword set
    feats: dict[str, Feat] = {}
    for _seg, proj, _t, _q in GOLD:
        if proj not in feats:
            feats[proj] = Feat(crib, proj)
    name_of = {f"{proj}::{f.by[i]['fqname']}": f.by[i].get("name", "")
               for proj, f in feats.items() for i in f.ids}

    def name_phrases(fq: str) -> list[str]:
        """The symbol's own name as searchable text: raw + spaced subtokens."""
        nm = name_of.get(fq, "")
        return [p for p in {nm, " ".join(_subtokens(nm))} if p]

    # two facets: semantic-only, and name-AUGMENTED (name is itself a keyword)
    kws_aug = {fq: list(ks) + name_phrases(fq) for fq, ks in kws.items()}
    phrases = sorted({p for ks in kws_aug.values() for p in ks})
    pv = dict(zip(phrases, crib.embedder.embed(phrases)))
    # uniq the phrases per symbol (dedupe name-vs-synth overlap)
    def _uniq(ks):
        return list(dict.fromkeys(ks))
    kv = {fq: [pv[p] for p in _uniq(ks) if p in pv] for fq, ks in kws.items()}

    def kw_maxsim(feat, proj, qv, facet):
        """Per-symbol MaxSim: best cosine from the query to any of the symbol's keyword
        phrases. Only symbols with generated keywords score (>0); others 0."""
        out = {}
        for idx, i in enumerate(feat.ids):
            fq = f"{proj}::{feat.by[i]['fqname']}"
            vecs = facet.get(fq)
            if vecs:
                out[idx] = max(_cos(qv, v) for v in vecs)
        return out

    # combiners: (feat, proj, q, qf, qv, k) -> [fqname]
    def c_dense(feat, proj, q, qf, qv, k):
        sc = {i: qf["cos"][i] for i in range(feat.n)}
        return [feat.by[feat.ids[i]]["fqname"] for i in order_from(sc, k)]

    def c_kw_only(facet):
        def fn(feat, proj, q, qf, qv, k):
            ks = kw_maxsim(feat, proj, qv, facet)
            return [feat.by[feat.ids[i]]["fqname"] for i in order_from(ks, k)]
        return fn

    def c_dense_kw(a, facet):
        def fn(feat, proj, q, qf, qv, k):
            ks = kw_maxsim(feat, proj, qv, facet)
            d = mm(qf["cos"])
            sc = {i: d[i] + a * ks.get(i, 0.0) for i in range(feat.n)}
            return [feat.by[feat.ids[i]]["fqname"] for i in order_from(sc, k)]
        return fn

    def c_covpc_wrap(a):
        base = c_covpc(a)
        def fn(feat, proj, q, qf, qv, k):
            return base(crib, proj, q, feat, qf, k)
        return fn

    def c_all(a_kw, a_cov, facet):
        """dense + covpc(name) + kw-maxsim — the full blend."""
        def fn(feat, proj, q, qf, qv, k):
            ks = kw_maxsim(feat, proj, qv, facet)
            cand = _cands(qf["bn"])
            d = mm(qf["cos"])
            from eval_fusion import _norm_over
            s = _norm_over(qf["bn"], cand)
            sc = {i: d[i] + a_cov * qf["cov"][i] * s.get(i, 0.0) + a_kw * ks.get(i, 0.0)
                  for i in range(feat.n)}
            return [feat.by[feat.ids[i]]["fqname"] for i in order_from(sc, k)]
        return fn

    def _normd(d):
        if not d:
            return {}
        lo, hi = min(d.values()), max(d.values())
        r = (hi - lo) or 1.0
        return {i: (v - lo) / r for i, v in d.items()}

    def three_facets(feat, proj, qf, qv):
        """Three INDEPENDENT per-symbol signals: exact name-coverage, semantic-kw MaxSim,
        dense. No shared/double-counted name."""
        cand = _cands(qf["bn"])
        from eval_fusion import _norm_over
        s = _norm_over(qf["bn"], cand)
        name = {i: qf["cov"][i] * s.get(i, 0.0) for i in cand if qf["cov"][i] > 0}
        kw = kw_maxsim(feat, proj, qv, kv)            # SEMANTIC-only (name is its own facet)
        dense = {i: qf["cos"][i] for i in range(feat.n)}
        return name, kw, dense

    def c_3rrf(feat, proj, q, qf, qv, k):
        from crib.retrieve import reciprocal_rank_fusion
        name, kw, dense = three_facets(feat, proj, qf, qv)
        lists = [[str(i) for i in order_from(dense, 40)],
                 [str(i) for i in sorted(name, key=name.get, reverse=True)[:40]],
                 [str(i) for i in sorted(kw, key=kw.get, reverse=True)[:40]]]
        fused = reciprocal_rank_fusion(lists)
        return [feat.by[feat.ids[int(x)]]["fqname"] for x in fused[:k]]

    def c_3wsum(wd, wn, wk):
        def fn(feat, proj, q, qf, qv, k):
            name, kw, dense = three_facets(feat, proj, qf, qv)
            dn, nn, kk = _normd(dense), _normd(name), _normd(kw)
            sc = {i: wd * dn.get(i, 0.0) + wn * nn.get(i, 0.0) + wk * kk.get(i, 0.0)
                  for i in range(feat.n)}
            return [feat.by[feat.ids[i]]["fqname"] for i in order_from(sc, k)]
        return fn

    def with_rerank(base):
        def fn(feat, proj, q, qf, qv, k):
            from crib.retrieve import reciprocal_rank_fusion
            b = base(feat, proj, q, qf, qv, max(k, 20))
            head = b[:20]
            by = {feat.by[i]["fqname"]: feat.by[i] for i in feat.ids}
            docs = [f"{fq}: {by.get(fq, {}).get('description', '')}" for fq in head]
            sc = crib.reranker.scores(q, docs)
            ro = [head[j] for j in sorted(range(len(head)), key=lambda j: sc[j], reverse=True)]
            return reciprocal_rank_fusion([b, ro])[:k]
        return fn

    combs = {
        "dense only": c_dense,
        "covpc·a1 (name)": c_covpc_wrap(1.0),
        "2sig covpc+kw(sem)+rr": with_rerank(c_all(1.0, 1.0, kv)),
        "3facet RRF": c_3rrf,
        "3facet wsum 1/1/1": c_3wsum(1.0, 1.0, 1.0),
        "3facet wsum 1/1.5/1": c_3wsum(1.0, 1.5, 1.0),
        "3facet RRF+rr": with_rerank(c_3rrf),
        "3facet wsum+rr": with_rerank(c_3wsum(1.0, 1.5, 1.0)),
    }
    ranks: dict[str, dict[str, list[int | None]]] = {n: {s: [] for s in SEGMENTS} for n in combs}
    try:
        for seg, proj, targets, q in GOLD:
            if proj not in feats:
                feats[proj] = Feat(crib, proj)
            feat = feats[proj]
            qf = feat.q(q)
            qv = crib.embedder.embed_query([q])[0]
            for n, fn in combs.items():
                fqs = fn(feat, proj, q, qf, qv, 20)
                ranks[n][seg].append(target_rank(fqs, targets))

        def r3(rs): return sum(1 for r in rs if r and r <= 3) / len(rs)
        def p1(rs): return sum(1 for r in rs if r == 1) / len(rs)
        def mrr(rs): return sum((1 / r) for r in rs if r) / len(rs)
        hdr = f"{'combiner':<20}" + "".join(f"{s[:7]+' R3':>11}" for s in SEGMENTS) + f"{'P@1':>7}{'MRR':>8}"
        print(hdr)
        print("-" * len(hdr))
        for n in combs:
            allr = [r for s in SEGMENTS for r in ranks[n][s]]
            cells = "".join(f"{r3(ranks[n][s]):>11.2f}" for s in SEGMENTS)
            print(f"{n:<20}{cells}{p1(allr):>7.2f}{mrr(allr):>8.3f}")
    finally:
        crib.close()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
